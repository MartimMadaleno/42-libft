#include "libft.h"

int	ft_strncmp(char *s1, char *s2, size_t n)
{
	size_t		i;
	t_u_char	c1;
	t_u_char	c2;

	i = 0;
	if (n == 0)
		return (0);
	while (s1[i] == s2[i] && (i < n - 1) && s1[i])
		i++;
	c1 = s1[i];
	c2 = s2[i];
	return (c1 - c2);
}
